    private void searchCustomer(){
       try {
             String cusId = custIdText.getText();
             CustomerDto cust =customerController.getCustomer(cusId);
             if(cust!=null){
             custData.setText(cust.getCustName()+" , "+ cust.getContactNo());
             }else{
             JOptionPane.showMessageDialog(this, "Customer not Found");
             }
        } catch (Exception ex) {
             Logger.getLogger(RentPanel.class.getName()).log(Level.SEVERE, null, ex);
         JOptionPane.showMessageDialog(this, ex.getMessage());
       }
    }
    
     private void searchCar() {
        try {
            String carId = carIdText.getText();
            CarDto car = carController.getCar(carId);
            if (car != null) {
                carData.setText(car.getModel() + ", " + car.getVehicleNo() + ", " + car.getRent());
            } else {
                JOptionPane.showMessageDialog(this, "Car Not Found");
            }
        } catch (Exception ex) {
            Logger.getLogger(RentPanel.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
     
    
