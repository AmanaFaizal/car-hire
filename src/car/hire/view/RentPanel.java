   private void saveRent() {
        try {
            RentDto rentDto = new RentDto(rentIdText.getText(),
                    fromDateText.getText(), toDateText.getText(), 
                    carIdText.getText(), custIdText.getText(), 
                    Double.parseDouble(totalText.getText()),Double.parseDouble( advanceText.getText()), 
                    Double.parseDouble(balanceText.getText()), isReturnText.getText());

            String result = rentController.saveRent(rentDto);
            JOptionPane.showMessageDialog(this, result);
            clear();
            loadAllRents();
        } catch (Exception ex) {
            Logger.getLogger(CustomerPanel.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }

    }

    

    private void loadAllRents() {

        try {
            String[] collumns = {"RentId", "CarId","CustId", " Duration","Balance","IsReturn"};
            DefaultTableModel dtm = new DefaultTableModel(collumns, 0) {
                @Override
                public boolean isCellEditable(int row, int column) {
                    return false;
                }
            };
            rentTable.setModel(dtm);

            ArrayList<RentDto> rents = rentController.getAllRents();

            for (RentDto rent : rents) {
                Object[] rowData = {rent.getRentId(), rent.getCarId(),rent.getCustId(),rent.getFromDate()+" - "+rent.getToDate(),rent.getBalance(),rent.getIsReturn()};
                    dtm.addRow(rowData);
            }
        } catch (Exception ex) {
            Logger.getLogger(CustomerPanel.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
    
     private void updateRent() {
            try {
            RentDto rentDto = new RentDto(rentIdText.getText(),
                    fromDateText.getText(), toDateText.getText(), 
                    carIdText.getText(), custIdText.getText(), 
                    Double.parseDouble(totalText.getText()),Double.parseDouble( advanceText.getText()), 
                    Double.parseDouble(balanceText.getText()), isReturnText.getText());

           
            String resp = rentController.updateRent(rentDto);
            JOptionPane.showMessageDialog(this, resp);
            clear();
            loadAllRents();
        } catch (Exception ex) {
            Logger.getLogger(CarPanel.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }
     
      private void searchRent() {
        try {
            String rent_id = rentTable.getValueAt(rentTable.getSelectedRow(), 0).toString();

            RentDto rentDto = rentController.getRent(rent_id);

            if (rentDto != null) {
                rentIdText.setText(rentDto.getRentId());
                fromDateText.setText(rentDto.getFromDate());
                toDateText.setText(rentDto.getToDate());
                carIdText.setText(rentDto.getCarId());
                custIdText.setText(rentDto.getCustId());
                totalText.setText(Double.toString(rentDto.getTotal()));
                advanceText.setText(Double.toString(rentDto.getAdvance()));
                balanceText.setText(Double.toString(rentDto.getBalance()));
            } else {
                JOptionPane.showMessageDialog(this, "Rent Not Found");
            }
        } catch (Exception ex) {
            Logger.getLogger(CarPanel.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }

